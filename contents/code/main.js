/* ============================================================================
 * Hello world KWin script
 * ========================================================================= */

(function () {
  "use strict";

  /* ----------------------------------------------------------------------------
   * Use DBus to display osd
   * ------------------------------------------------------------------------- */
  function osd(text, icon) {
    if (!icon) {
      icon = "kwin";
    }

    callDBus(
      'org.kde.plasmashell',
      '/org/kde/osdService',
      'org.kde.osdService',
      'showText',
      icon, text
    );
  }

  /* ----------------------------------------------------------------------------
   * Init script
   * ------------------------------------------------------------------------- */
  function init() {

    print("Hello world!");

    osd("Hello world!");

  };

  init();

})();

